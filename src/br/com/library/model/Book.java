package br.com.library.model;

public class Book {
	private String bookTitle;
	private String author;
	private short page;
	private int isbn;
	private String typeBook;
	
	
	
	
	
	public String getBookTitle() {
		return bookTitle;
	}
	public String getAuthor() {
		return author;
	}
	public short getPage() {
		return page;
	}
	public int getIsbn() {
		return isbn;
	}
	public String getTypeBook() {
		return typeBook;
	}
}
